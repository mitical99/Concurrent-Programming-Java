package rs.bg.ac.etf.kdp.lab2019;

public class Film {
	private String director;
	private String movie;
	public Film(String director,String movie) {
		this.director = director;
		this.movie = movie;
	}
	public String getDirector() {
		return director;
	}
	public String getMovie() {
		return movie;
	}
	
}
